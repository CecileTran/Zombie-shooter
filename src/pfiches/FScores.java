    public void initialiser() {
        tabLabel = new JLabel[LIGNES][COLONNES];

        GridLayout gest = new GridLayout(0, COLONNES);
        lScores.setLayout(gest);
        for (int i = 0; i < tabLabel.length; i++) {
            for (int j = 0; j < tabLabel[i].length; j++) {
                //creer un label
                JLabel lab = new JLabel();
                //dÃ©finir la taille du label
                Dimension dim = new Dimension(60, 60);
                lab.setPreferredSize(dim);
                //ajouter le label dans le tableau
                tabLabel[i][j] = lab;
                //ajouter le label dans le panel
                lScores.add(tabLabel[i][j]);
                tabLabel[i][j].setHorizontalAlignment(JLabel.CENTER);
                tabLabel[i][j].setFont(new java.awt.Font("Verdana", 1, 14));
                tabLabel[i][j].setForeground(new java.awt.Color(255, 255, 255));
                tabLabel[i][j].setBorder(javax.swing.BorderFactory.createBevelBorder(javax.swing.border.BevelBorder.RAISED));
            }
        }
        this.pack(); //mettre a jour l'affichage
        jeu = new Jeu("Toto");

        tabLabel[0][0].setText("Rang");
        tabLabel[0][0].setFont(new java.awt.Font("Lucida Handwriting", 1, 18));
        tabLabel[0][1].setText("Nom Joueur");
        tabLabel[0][1].setFont(new java.awt.Font("Lucida Handwriting", 1, 18));
        tabLabel[0][2].setText("Score");
        tabLabel[0][2].setFont(new java.awt.Font("Lucida Handwriting", 1, 18));
        for (int d = 1; d < LIGNES; d++) {
            tabLabel[d][0].setText("" + d);
        }
        jeu.trierScore(tabLabel);

    }

    public void initFond() {
        //acces au toolkit
        Toolkit t = Toolkit.getDefaultToolkit();
        //acces a l'image
        img = t.getImage(DOSS_IMAGES + "FondScores.jpg");
        img = img.getScaledInstance(600, 500, Image.SCALE_DEFAULT);

        layeredPane.setPreferredSize(new Dimension(600, 500));

        this.setContentPane(layeredPane);

        layeredPane.add(lFond, 1);

        lFond.setIcon(new ImageIcon(img));
        lFond.setPreferredSize(new Dimension(600, 500));
        lFond.setBounds(0, 0, 600, 500);

        layeredPane.add(lScores, 0);
        layeredPane.add(bRetour, 0);
    }

